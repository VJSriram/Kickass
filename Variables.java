
public class Variables {

	public static void main(String[] args) {
		
		/* 
		 * byte (number, 1 byte)
		 * short (number, 2 bytes)
		 * int (number, 4 bytes)
		 * long (number, 8 bytes)
		 * float (float number, 4 bytes)
		 * double (double number, 8 bytes)
		 * char (a character, 2 bytes)
		 * boolean (true or false, 1 bytes)
		 */
		
		// dataType variableName;
				int my_variable = 10;
				short my_short = 20;
				byte my_byte = 5;
				long my_long = 152;
				
				float my_decimal = (float)4.5;
				double my_double = 11.56;
				char my_char = 'A';
				Boolean is_true = false;
		
		System.out.println(my_variable);
		System.out.println(my_short);
		System.out.println(my_byte);
		System.out.println(my_long);
		
		System.out.println(my_decimal);
		System.out.println(my_double);
		System.out.println(my_char);
		System.out.println(is_true);

	}

}
