
public class JavaVariables {

	public static void main(String[] args) {
		/*
		Syntax:  modifier dataType variableName;  */
		
		public int year;
		int year; // modifier is optional
		
		/*variables can be assigned values either separately or on declaration
        
        Syntax: variableName = value; //separate line assignment */
		
		year = 2018;
		

		/*modifier dataType variableName = value; //same line assignment */
		
		public int year = 2018;
		
		System.out.println(year);
		

	}

}
